public class TEEVersionModel {
    String name;
    String sem;
    String crscd;
    String qpid;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSem() {
        return sem;
    }

    public void setSem(String sem) {
        this.sem = sem;
    }

    public String getCrscd() {
        return crscd;
    }

    public void setCrscd(String crscd) {
        this.crscd = crscd;
    }

    public String getQpid() {
        return qpid;
    }

    public void setQpid(String qpid) {
        this.qpid = qpid;
    }
}
